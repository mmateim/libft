#include "libft.h"

char	*ft_strnew(size_t size)
{
	size_t	i;
	char	*s;

	s = (char*)malloc(sizeof(char) * (size + 1));
	i = 0;
	if (s == 0)
		return (0);
	while (i <= size)
	{
		s[i] = '\0';
		i++;
	}
	return (s);
}
