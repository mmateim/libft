#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	int		sgn;
	long	nr;

	nr = 0;
	sgn = 1;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
	{
		i++;
	}
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sgn = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		nr = nr * 10 + (str[i] - '0');
		i++;
	}
	return (nr * sgn);
}
